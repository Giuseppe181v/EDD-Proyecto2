    private void checkInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkInActionPerformed
        // TODO add your handling code here:
        String cedula = id.getText();
        if(cedula.isBlank()){
            JOptionPane.showMessageDialog(null, "Debes ingresar la c√©dula");
        }else{
            int cInt = Integer.parseInt(cedula);
            Reserva reserva = arbolReserva.buscar(cInt);
            arbolReserva.eliminar(cInt);
            
            if(reserva == null){
                JOptionPane.showMessageDialog(null, "No existe la reserva para este usuario");
            }else{
                String numHab, ci, nombre, apellido, correo, genero, celular, fechaLlegada = "";
                numHab = "";
                Lista habitacionesOcupadas = tablaEstado.habitacionesOcupadas();
                for (int i = 1; i <= 300; i++) {
                    if(!habitacionesOcupadas.contiene(i)){
                        numHab = Integer.toString(i);
                        break;
                    }
                }
                ci = reserva.getCi();
                nombre = reserva.getNombre();
                apellido = reserva.getApellido();
                correo = reserva.getCorreo();
                genero = reserva.getGenero();
                celular = reserva.getCelular();
                fechaLlegada = reserva.getFechaLlegada();
                //String numHabitacion, String ci, String nombre, String apellido, String correo, String genero, String celular, String fechaLlegada
                Estado estado = new Estado(numHab, ci, nombre, apellido, correo, genero, celular, fechaLlegada);
                tablaEstado.insertar(estado);
                
                
                output.setText("El usuario ha ingresado en el sistema");
                
            }
        }
    }//GEN-LAST:event_checkInActionPerformed

