    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        
        String cedula = id.getText();
        if(cedula.isBlank()){
            JOptionPane.showMessageDialog(null,"Necesitas ingresar la c√©dula");
            
        }
        
        else{
            int cId = Integer.parseInt(cedula);
            Reserva reserva = arbolReserva.buscar(cId);
            if(reserva == null){
                JOptionPane.showMessageDialog(null, "No existe esta reserva");
            }else{
                JOptionPane.showMessageDialog(null, "Reserva encontrada");
                this.dispose();
                BuscarReservacion busqueda = new BuscarReservacion(reserva, tablaEstado, arbolReserva, arbolHistorico);
                busqueda.setVisible(true);
            }
            
        }
    }//GEN-LAST:event_buscarActionPerformed

